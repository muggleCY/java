public class HolidayDeleteException extends Exception {
    public HolidayDeleteException() {
    }

    public HolidayDeleteException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public HolidayDeleteException(String arg0) {
        super(arg0);
    }

    public HolidayDeleteException(Throwable arg0) {
        super(arg0);
    }

}
