public class DeptModifyException extends Exception {
    public DeptModifyException() {
        super();
    }

    public DeptModifyException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public DeptModifyException(String arg0) {
        super(arg0);
    }

    public DeptModifyException(Throwable arg0) {
        super(arg0);
    }

}
